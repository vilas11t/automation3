package package1;

public class SwitchPractice {
	
	public static void main(String[] args) {
		int ch=2;
		switch(ch) {
		case 1:
			System.out.println("this is case 1 code");
			break;
						
		case 2:
			System.out.println("this is case 2 code");
			break;
			
			
		default:
			System.out.println("This is default case");
			break;
			
		}
	}

}
